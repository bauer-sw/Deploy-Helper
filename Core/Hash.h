#pragma once

#include <functional>

namespace Core {

  template<typename T> 
  void hash_combine(size_t& seed, const T& v) {
    seed ^= std::hash<T>()(v) + 0x9e3779b9 + (seed << 6) + (seed >> 2);    
  }

}
